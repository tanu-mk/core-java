class ECommerce{

	static String type;
	static String product;
	static int price;
	static String color;
	
	
	public static void setType(String tp){
	     type = tp;
	}
	public static String getType(){
		return type;
	}


	public static void setProduct(String pro){
		product = pro;
	}
	public static String getProduct(){
		return product;
	}


	public static void setPrice(int pr){
		price = pr;
	}
	public static int getPrice(){
		return price;
	}
	

	public static void setColor(String clr){
		color = clr;
	}
	public static String getColor(){
		return color;
	}





}