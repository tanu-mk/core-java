class Furniture{

	static String name;
	static int price;
	static String color;
	
	
	public static void setName(String nm){
		name = nm;
	}
	public static String getName(){
		return name;
	}


	public static void setPrice(int pr){
		price = pr;
	}
	public static int getPrice(){
		return price;
	}


	public static void setColor(String clr){
		color = clr;
	}
	public static String getColor(){
		return color;
	}

}