class RailwayStation{

	static String stationName;
	static String ticketNumber;
	static String trainName;
	
	
	
	public static void setStationName(String sm){
		stationName = sm;
	}
	public static String getStationName(){
		return stationName;
	}


	public static void setTicketNumber(String tn){
		ticketNumber = tn;
	}
	public static String getTicketNumber(){
		return ticketNumber;
	}


	public static void setTrainName(String tnn){
		trainName = tnn;
	}
	public static String getTrainName(){
		return trainName;
	}

}